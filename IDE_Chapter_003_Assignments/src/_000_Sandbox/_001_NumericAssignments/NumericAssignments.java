package _000_Sandbox._001_NumericAssignments;

public class NumericAssignments {
    public static void main(String[] args){
        int x = 0x0001;
        System.out.println("0x0001 \t\t" + x);

        int y = 0x7fffffff;
        System.out.println("0x7fffffff \t" + y);

        int z = 0xDeadCafe;
        System.out.println("0xDeadCafe \t" + z);

        int w = 0xCafe;
        System.out.println("0xCafe \t\t" + w);
    }
}
