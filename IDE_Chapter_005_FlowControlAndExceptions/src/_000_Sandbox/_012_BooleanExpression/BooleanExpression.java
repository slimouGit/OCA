public class BooleanExpression {
    public static void main(String[] args){
        boolean x = false;

        if (x==false){
            System.out.println(x);
        }
        if (x=false){
            System.out.println(x);
        }
        if (x=true){
            System.out.println(x);
        }
    }
}
